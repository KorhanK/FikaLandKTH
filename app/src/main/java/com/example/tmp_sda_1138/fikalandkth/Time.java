class Time {
    private int month;
    private int turns;

    public Time(int month, int turns){
        this.month = month;
        this.turns = turns;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getTurns() {
        return turns;
    }

    public void setTurns(int turns) {
        this.turns = turns;
    }
}


